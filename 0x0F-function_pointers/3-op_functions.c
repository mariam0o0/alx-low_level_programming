#include <stdio.h>
#include <stdlib.h>
/**
 * op_add - addition
 * @a: fist integer
 * @b: second integer
 * Return: the sum of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - substraction
 * @a: first integer
 * @b: second integer
 * Return: the difference of a and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplication
 * @a: first integer
 * @b: second integer
 * Return: the product of a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - division
 * @a: first integer
 * @b: second integer
 * Return: the result of division of a and b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - dreminder of division
 * @a: first integer
 * @b: second integer
 * Return: the reminder of the division of a and b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
